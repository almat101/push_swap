#include "ft_printf.h"

int	write_str(char *str)
{
	int	i;

	if (!str)
	{
		str = "(null)";
		i = 6;
	}
	i = 0;
	while (str[i])
	{
		write_char(str[i]);
		i++;
	}
	return (i);
}
